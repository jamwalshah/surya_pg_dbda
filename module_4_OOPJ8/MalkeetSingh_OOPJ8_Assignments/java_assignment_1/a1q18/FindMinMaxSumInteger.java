public class FindMinMaxSumInteger {
    public static void main(String[] args) {
        Integer I1 = new Integer(5);
        Integer I2 = new Integer(7);
        System.out.println("I1 : "+I1);
        System.out.println("I2 : "+I2);
        System.out.println("Maximum is  "+Integer.max(I1,I2));
        System.out.println("Minimum is  "+Integer.min(I1, I2));
        System.out.println("Sum is      "+Integer.sum(I1, I2));
    }
    
}
