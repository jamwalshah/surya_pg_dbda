public class MinMaxSumOfDouble {
    public static void main(String[] args) {
        Double D1 = new Double(1561.465456);
        Double D2 = new Double(438.534544);
        System.out.println("Minimum : "+Double.min(D1, D2));
        System.out.println("maximum : "+Double.max(D1, D2));
        System.out.println("Sum     : "+Double.sum(D1, D2));
    }
    
}
