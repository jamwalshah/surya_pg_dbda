public class MinMaxSumOfLong {
    public static void main(String[] args) {
        long l1 = 12, l2 = 13;
        System.out.println("l1      is  "+l1);
        System.out.println("l2      is  "+l2);
        System.out.println("Maximun is  "+Long.max(l1, l2));
        System.out.println("Minimum is  "+Long.min(l1, l2));
        System.out.println("sum     is  "+Long.sum(l1, l2));
    }
    
}
